# CALCULAR EL AREA Y PERIMETRO DE UN CIRCULO
# ENTRADAS DE DATOS
# DEFINIR LAS VARIABLES DE ENTRADA

pi = 3.1416
radio = float (input("introduce el radio del circulo"))

# AREA DEL CIRCULO 
# PERIMETRO DEL CIRCULO

area = pi*(radio**2)
peri=2*radio*pi

# SALIDA DE DATOS
print("area=",area)
print("perimetro=",peri)